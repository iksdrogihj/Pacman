vector<vector<int> >* Maze::getMaze()
{
    if(0 == maze)
        maze = new vector<vector<int> >(30, vector<int>(30));;
    return maze;
}

void Maze::Draw(){
    
    // Draws maze
    for(size_t row = 0; row < Maze::getMaze()->size(); ++row){
        for (size_t collumn = 0; collumn < maze->size(); ++collumn) {
            switch(maze->at(collumn).at(row)) {
                            
                case Cell::Type::wall:
                    glPushMatrix();
                    glMaterialfv(GL_FRONT, GL_DIFFUSE, wall_material);
                    glTranslated(row, collumn, 0);
                    glutSolidCube(0.9F);
                    glPopMatrix();
                    break;
                            
                case Cell::Type::ball:
                    glPushMatrix();
                    glMaterialfv(GL_FRONT, GL_DIFFUSE, pill_material);
                    glTranslated(row, collumn, 0);
                    glutSolidSphere(0.1F, 10.0F, 10.0F);
                    glPopMatrix();
                    break;
                            
                case Cell::Type::powerup:
                    glPushMatrix();
                    glMaterialfv(GL_FRONT, GL_DIFFUSE, power_up_material);
                    glTranslated(row, collumn, 0);
                    glutSolidSphere(0.3F, 10.0F, 10.0F);
                    glPopMatrix();
                    break;
                            
                case Cell::Type::empty_space:
                    break;
                            
                    default:
                    break;
                    }
                }
            }


        }

int Maze::cellTypeAt(Coord& a){
    
    return maze->at(a.y).at(a.x);
}
